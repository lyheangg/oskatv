function populateMatches(roundFilter = "all") {
  const tableBody = document.getElementById("matchesTable");
  tableBody.innerHTML = "";
  let filteredMatches = matches;
  if (roundFilter !== "all") {
    const roundIndex = parseInt(roundFilter, 10) - 1; // Convert round to index
    // Assuming rounds are split evenly among matches, adjust this logic as necessary
    const matchesPerRound = matches.length / 8;
    const startIndex = roundIndex * matchesPerRound;
    const endIndex = startIndex + matchesPerRound;
    filteredMatches = matches.slice(startIndex, endIndex);
  }
  filteredMatches.forEach((match) => {
    const row = document.createElement("tr");
    row.innerHTML = `
        <td>${match.id}</td>
        <td>${match.date}</td>
        <td>${match.team1}</td>
        <td>VS</td>
        <td>${match.team2}</td>
        <td>${match.winner}</td>`;
    tableBody.appendChild(row);
  });
}

